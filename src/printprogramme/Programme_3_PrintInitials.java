package printprogramme;

/**
 * Write a programme that will print your initials to standard output in letters that are
 * nine line tall. Each big letter should be made up of a bunch of *'s. for example, if your
 * initials were "JAJA", then the output would look something like:
 */
public class Programme_3_PrintInitials {
    public static void main(String[] args) {
        System.out.println("*********  ********  *********  ********");
        System.out.println("      **   **    **        **   **    **");
        System.out.println("      **   **    **        **   **    **");
        System.out.println("      **   **    **        **   **    **");
        System.out.println("      **   ********        **   ********");
        System.out.println("  **  **   **    **    **  **   **    **");
        System.out.println("  **  **   **    **    **  **   **    **");
        System.out.println("   ****    **    **     ****    **    **");
        System.out.println("    **     **    **      **     **    **");
    }

}
